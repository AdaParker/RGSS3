#    用法是:分歧条件-脚本-
#    player_map_pos?(m, x, y)  #判断玩家当前位置是否为(m, x, y) m 为地图ID
#    player_pos?(x ,y)         #判断玩家当前位置是否为(x,y)
#    event_pos?(x, y)          #判断本事件当前坐标位置是否为(x,y)
#  另外：get_character(param)  是个好东西
