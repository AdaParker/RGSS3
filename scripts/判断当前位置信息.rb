$VIPArcherScript ||= {};$VIPArcherScript[:location] = 20141203
#--------------------------------------------------------------------------------
class Game_Interpreter
  #--------------------------------------------------------------------------
  # ● 判断玩家当前坐标位置是否为(m, x, y)
  #--------------------------------------------------------------------------
  def player_map_pos?(m, x, y)
    $game_map.map_id == m && player_pos?(x, y)
  end
  #--------------------------------------------------------------------------
  # ● 判断玩家当前坐标位置是否为(x,y)
  #--------------------------------------------------------------------------
  def player_pos?(x, y)
    $game_player.pos?(x, y)
  end
  #--------------------------------------------------------------------------
  # ● 判断本事件当前坐标位置是否为(x,y)
  #--------------------------------------------------------------------------
  def event_pos?(x, y)
    $game_map.events[@event_id].pos?(x, y)
  end
end